@app.route('/admin-login', methods=['GET','POST'])
def admin_login():

    if request.method == 'GET':
        return render_template('/admin/admin_login.html')
    
    email = request.form['email']
    password = request.form['password']

    conn = get_db_connection()
    cursor = conn.cursor(dictionary=True)
    cursor.execute("SELECT * FROM admin WHERE email = %s", (email,))
    admin = cursor.fetchone()
    cursor.close()
    conn.close()

    # ❌ Email not found
    if not admin:
        flash("Email not found! Please register.", "danger")
        return redirect(url_for('admin_signup'))
    
    # 🔐 Password check
    stored_hashed_password = admin['password']

    if isinstance(stored_hashed_password, str):
        stored_hashed_password = stored_hashed_password.encode('utf-8')

    if not bcrypt.checkpw(password.encode('utf-8'), stored_hashed_password):
        flash("Incorrect Password! Try Again", "danger")
        return redirect(url_for('admin_login'))
    
    # 🔒 Approval check
    if not admin.get('is_approved', False):
        flash("Your account is pending approval by Super Admin.", "danger")
        return redirect(url_for('admin_login'))
    
    # ✅ Session
    session['admin_id'] = admin['id']
    session['admin_name'] = admin['name']
    session['admin_email'] = admin['email']
    session['admin_role'] = admin['role'] if admin.get('role') else 'admin'
    session['admin_profile'] = admin['profile_image'] if admin.get('profile_image') else 'default.png'
    

    flash("Login Successful.", "success")
    return redirect(url_for('dashboard'))


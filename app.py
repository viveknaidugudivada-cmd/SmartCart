@app.route('/reset-password/<role>/<token>', methods=['GET', 'POST'])
def reset_password(role, token):

    # ✅ Validate role
    if role not in ['user', 'admin']:
        return "Invalid role", 400

    # 🔐 Validate token
    try:
        email = serializer.loads(token, salt='password-reset-salt', max_age=600)
    except SignatureExpired:
        flash("Reset link expired!", "danger")
        return redirect(url_for('forgot_password', role=role))
    except BadSignature:
        flash("Invalid reset link!", "danger")
        return redirect(url_for('forgot_password', role=role))

    # ✅ Show reset page
    if request.method == 'GET':
        return render_template(f'{role}/reset_password.html', role=role)

    # 🔐 Get new password
    new_password = request.form['password']
    confirm_password = request.form.get('confirm_password')

    # ✅ Validate password
    if new_password != confirm_password:
        flash("Passwords do not match!", "danger")
        return redirect(request.url)

    if len(new_password) < 6:
        flash("Password must be at least 6 characters!", "danger")
        return redirect(request.url)

    # 🔐 Hash password
    hashed_password = bcrypt.hashpw(new_password.encode('utf-8'), bcrypt.gensalt()).decode('utf-8')

    conn = get_db_connection()
    cursor = conn.cursor()

    if role == 'user':
     cursor.execute(
        "UPDATE users SET password=%s WHERE email=%s",
        (hashed_password, email)
    )
    else:
     cursor.execute(
        "UPDATE admin SET password=%s WHERE email=%s",
        (hashed_password, email)
    )

    conn.commit()

    cursor.close()
    conn.close()

    flash("Password reset successful!", "success")

    return redirect(url_for('user_login' if role == 'user' else 'admin_login'))

